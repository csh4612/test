package test01;

public class Print {

	public static void main(String[] args) {
		int i = 7;
		int j = 5;
		int sum = i+j;
		
		System.out.println(i);
		System.out.println(j);
		System.out.println(i+j);
		System.out.println(sum);
		
		double dec1 = 1.5;
		double dec2 = 2.5;
		double decSum = dec1+dec2;
		
		System.out.println(decSum);
		
		System.out.println(1!=2);
		
		boolean smart = 1!=2;
		System.out.println(smart);
		
		/* 
		 * 자바의 데이터 타입은 
		 * 1. 숫자 저장
		 *  1-1 정수 : byte, short, int, long
		 *  1-2 실수 : float, double 
		 *  
		 * 2. 문자 저장 : char
		 * 
		 * 3. 논리 저장 : boolean
		 * 
		 * 데이터 타입이 소문자, 원시형 데이터 타입
		 * 
		 */
		
	}
}
